function sum(a, b) {
    let result;
    result = a + b;
    return result;
}

function min(a, b) {
    let result;
    result = a - b;
    return result;
}

function mult(a, b) {
    let result;
    result = a * b;
    return result;
}

function div(a, b) {
    let result;
    result = a / b;
    return result;
}

function mod(a, b) {
    let result;
    result = a % b;
    return result;
}

