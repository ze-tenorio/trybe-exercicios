//variavel que armazena um objeto com os tipos de frutas e quantidade
let fruitsList = {};

//loop que percorre o array basket
for (let fruit of basket) {
    //preenchendo o objeto fruitsList
    fruitsList[fruit] = 0;
}

//percorrendo dnv o array basket
for (let fruit of basket) {
    for (let fruitType in fruitsList) {
        if (fruit === fruitType) {
            fruitsList[fruitType] += 1;
        }
    }
}

let summaries = [];

//criando o output
for (fruit in fruitsList) {
    let message = `${fruitsList[fruit]} ${fruit}`;
    if (fruitsList[fruit] > 1) message += 's';
    summaries.push(message);
}

